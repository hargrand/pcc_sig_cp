"""Sample 05.04 - String functions."""

text = "Hello, World!"

print("Converted to lower case:", text.lower())
print("Converted to upper case:", text.upper())
print("Replace text           :", text.replace("World", "Python"))
print("Position of first 'o'  :", text.find("o"))
print("Length of the text     :", len(text))
print("Original text          :", text)
